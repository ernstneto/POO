package tarefa3;

public class Cidade {
	private final String nome;
	private final String siglaUF;
	
	public Cidade(String nome, String siglaUF) {
		this.nome = nome;
		this.siglaUF = siglaUF;
	}

	public String getNome() {
		return nome;
	}

	public String getSiglaUF() {
		return siglaUF;
	}
	
	
}
